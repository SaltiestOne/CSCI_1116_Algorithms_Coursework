package AlgEtc;

public class StateCapital {
	  
	  private String state, capital;
	  
	  StateCapital(String state, String capital) {
		  
		  this.state = state;
		  
		  this.capital = capital;
	  }
	  
	  public String getState() {
		  
		  return this.state;
	  }
	  
	  public String getCapital() {
		  
		  return this.capital;
	  }
}
